# 描述:关系运算符
a = 100
b = 200
c = 300
d = 300

res1 = a > b    # 100 > 200
print(res1)     # False

res2 = a < b    # 100 < 200
print(res2)     # True

res3 = c == d   # 300 == 300
print(res3)     # True

res4 = c >= a   # 300 >= 100      # >=  表示大于或等于
print(res4)     # True

res5 = c <= d   # 300 <= 300
print(res5)     # True

res6 = a != b   # 100 != 200
print(res6)     # True








